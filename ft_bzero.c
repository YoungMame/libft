#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	unsigned char	*a;

	a = (unsigned char *)s;
	i = 0;
	while (i++ < n)
	{
		*a = '\0';
		a++;
	}
	return ;
}
